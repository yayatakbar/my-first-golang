package main

import "fmt"

func main() {

	//Number
	fmt.Println("satu =", 1)
	fmt.Println("dua =", 2)
	fmt.Println("tiga koma lima =", 3.5)

	//Boolean
	fmt.Println("True =", true)
	fmt.Println("False =", false)

	//String
	fmt.Println("Hello World")
	fmt.Println(len("Hello World"))
	fmt.Println(("Hello World"[1]))

}
